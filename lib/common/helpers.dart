/** 
 * Print Error * - It prints the error with a common format
 */
void printError(dynamic e) {
  print('#--------------------- ERROR -----------------#');
  print(e);
  print('#--------------------- ERROR -----------------#');
}

/** 
 * Print Warn * - It prints a warn with a common format
 */
void printWarn(dynamic w) {
  print('#--------------------- WARNING -----------------#');
  print(w);
  print('#--------------------- WARNING -----------------#');
}